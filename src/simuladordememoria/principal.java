    private void IniProcesoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IniProcesoActionPerformed

         
                    String Nombre=Nombrep.getText();                           //Nombre del proceso
                    int Hojas= Integer.parseInt(Npaginas.getSelectedItem().toString());          //Numero de Hojas
                    
                    if(memoria+(Hojas*4) > 16){
                            //SE CREAN CUANDO LA MEMORIA NO ALCANZA Y TIENEN QUE ESPERAR 
                            contador++;
                            proceso.InsertarProceso(Nombre, Hojas,1, contador);
                            JOptionPane.showMessageDialog(null,"Proceso creado y agregado a la lista de espera, la memoria es insuficiente");
                            //contador++;
                            IniciarTodo();
                            //memoria=memoria+(Hojas*4);
                            
                            
                            
                    }else{
                            //SE CREAN CUANDO LA MEMORIA ESTA DISPONIBLE 
                            contador++;
                            proceso.InsertarProceso(Nombre, Hojas,0, contador);
                            JOptionPane.showMessageDialog(null,"Proceso Creado y Agregado a la memoria Fisica");
                            //contador++;
                            IniciarTodo();
                            memoria=memoria+(Hojas*4);
                           
                    }
                   
           
        
        
           
    }//GEN-LAST:event_IniProcesoActionPerformed

